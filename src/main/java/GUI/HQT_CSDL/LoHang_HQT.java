    private void formMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_formMouseClicked
        // Double clicked
        long currentTime = System.currentTimeMillis();

        if (clickCount == 1 && (currentTime - lastClickTime) <= 200) {
            // Xử lý sự kiện double click
            System.out.println("Double clicked!");
            chiTietKhoDAO = new ChiTietKhoDAO();
            // Lấy danh sách mã sản phẩm trong lô hàng
            List<String> sanPhamList = new ArrayList<>();
            sanPhamList = chiTietKhoDAO.getSanPhamListByMaLoHang(khoDTO.getMaLoHangString());

            model.setRowCount(0);

            Object obj[] = new Object[100];
            for (int i = 0; i < sanPhamList.size(); i++) {
                sanPhamDAO = new SanPhamDAO();
                obj[0] = sanPhamDAO.tim(sanPhamList.get(i)).getMaSPString();
                obj[1] = sanPhamDAO.tim(sanPhamList.get(i)).getTenSPString();
                obj[2] = sanPhamDAO.tim(sanPhamList.get(i)).getPhanLoaiString();
                obj[3] = chiTietKhoDAO.tim(khoDTO.getMaLoHangString(), sanPhamDAO.tim(sanPhamList.get(i)).getMaSPString()).getSoLuong();
                model.addRow(obj);
            }

            jDialogDetailLH.setVisible(true);
            revalidate();
            repaint();
            // Reset lại số lần click và thời gian sau khi xử lý xong
            clickCount = 0;
            lastClickTime = 0;
        } else {
            // Lưu số lần click và thời gian hiện tại
            clickCount = 1;
            lastClickTime = currentTime;
            requestFocus();
        }
    }// GEN-LAST:event_formMouseClicked

