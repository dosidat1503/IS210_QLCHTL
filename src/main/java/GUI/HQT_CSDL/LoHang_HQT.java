    private void jPanelSaveMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jPanelSaveMouseClicked
        // TODO add your handling code here:
        ChiTietKhoDTO chiTietKhoDTO = new ChiTietKhoDTO();
        chiTietKhoDTO.setMaLoHang(jTextFieldMaLH.getText());
        chiTietKhoDTO.setMaSP(jTextFieldMaSP_LH.getText());
        chiTietKhoDTO.setSoLuong(Integer.valueOf(jTextField6.getText()));

        chiTietKhoDAO = new ChiTietKhoDAO();
        try {
            chiTietKhoDAO.insertLoHang(chiTietKhoDTO);
            sanPhamDAO = new SanPhamDAO();
            SanPhamDTO sanPhamDTO = new SanPhamDTO();
            sanPhamDTO = sanPhamDAO.tim(chiTietKhoDTO.getMaSP());
            Object obj[] = new Object[100];
            obj[0] = sanPhamDTO.getMaSPString();
            obj[1] = sanPhamDTO.getTenSPString();
            obj[2] = sanPhamDTO.getPhanLoaiString();
            obj[3] = chiTietKhoDTO.getSoLuong();
            model.addRow(obj);
            jDialogAddSP_LH.dispose();
        } catch (SQLException ex) {
            if (ex.getErrorCode() == -20000) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } else if (ex.getErrorCode() == -21000) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            ex.printStackTrace();
            Logger.getLogger(LoHang_HQT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }// GEN-LAST:event_jPanelSaveMouseClicked

