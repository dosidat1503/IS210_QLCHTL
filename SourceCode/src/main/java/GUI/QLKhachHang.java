    private void jLabel22MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel22MouseClicked
        // TODO add your handling code here:
        Component[] components = jPanel4.getComponents();

        // Iterate over the components in reverse order
        for (int i = components.length - 1; i >= 0; i--) {
            Component component = components[i];

            // Check if the component is an instance of Product
            if (component instanceof KhachHang) {
                KhachHang khachHang = (KhachHang) component;

                // Check if the customer needs to be deleted
                if (khachHang.isSelect()) {
                    // Remove the customer from Database
                    try {
                        if (khachHangBUS.xoa_proc(khachHang.getKhachHangDTO())) {
                            // Remove the customer from the panel and update the UI
                            jPanel4.remove(khachHang);
                            jPanel4.revalidate();
                            jPanel4.repaint();
                            System.out.println("Xoa KH thanh cong!");
                            jDialogDeleteKH.dispose();
                        } else {
                            JOptionPane.showMessageDialog(null, "Xóa KH không được!");
                        }
                    } catch (Exception e) {
                        // TODO Auto-generated catch block
                        // e.printStackTrace();
                        JOptionPane.showMessageDialog(null, "Lỗi ràng buộc khóa ngoại");
                        System.out.println("Lỗi ràng buộc khóa ngoại");
                    }
                }
            }
        }
    }//GEN-LAST:event_jLabel22MouseClicked

