    private void jPanelSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelSaveMouseClicked
        // TODO add your handling code here:
        KhachHangDTO khachHangDTO = new KhachHangDTO();
        khachHangDTO.setMaKHString(jTextFieldMaKH.getText());
        khachHangDTO.setTenKHString(jTextFieldTenKH.getText());
        khachHangDTO.setSDTKHString(jTextFieldSDTKH.getText());
        khachHangDTO.setNgayDangKyDate(new Date());

        khachHangDAO = new KhachHangDAO();
        try {
            if (khachHangDAO.insertKhachHang(khachHangDTO)) {
                jDialogAddKH.dispose();
                jPanel4.removeAll();
                KhachHangBUS khachHangBUS = new KhachHangBUS();
                for (int i = 0; i < khachHangBUS.getlist_KhachHangDTOs().size(); i++) {
                    KhachHang khachHang = new KhachHang(khachHangBUS.getlist_KhachHangDTOs().get(i));
                    jPanel4.add(khachHang);
                }
                jPanel4.revalidate();
                jPanel4.repaint();
            } else {
                JOptionPane.showMessageDialog(null, "Error");
            }
        } catch (SQLException ex) {
            if (ex.getErrorCode() == -20000) {
                // Bắt và xử lý lỗi do RAISE_APPLICATION_ERROR
                String errorMessage = ex.getMessage();
                JOptionPane.showMessageDialog(null, errorMessage);
                System.out.println("Lỗi từ cơ sở dữ liệu: " + errorMessage);
            } else {
                // Xử lý các lỗi khác
                ex.printStackTrace();
                Logger.getLogger(QLKhachHang.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jPanelSaveMouseClicked

